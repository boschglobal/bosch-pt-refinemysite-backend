      }
    }
  }

  @Test
  fun `is reassigned by the CSM to CR of another company, the CR is notified`() {
    eventStreamGenerator.submitTask(
        auditUserReference = CSM_USER, eventType = TaskEventEnumAvro.ASSIGNED) {
